      <h6>Keep your documentation up-to-date</h6>

      <p><i>If you have ever found yourself trying to decipher instructions from an outdated user manual, we bet you
          got more frustrated than trying it without a user guide at all. </i></p>

      <p>As anyone working in customer support can tell you, <strong>out of date documentation can be worse than
          having no
          documentation at all:</strong> screenshots and videos are the most important visual clues that inform a user
        if they
        are still on the right track. If the interface changes, and the screenshots aren’t updated, the user will be
        lost and confused.
      </p>

      <p>
        WalkHub automatically tests the process every time a Walkthrough is played. Not only do we check if the
        element you are interacting with is still present, but we also <strong>verify if the underlying user story
          still
          works.</strong>
        As a user you can be <strong>sure</strong> that the Walkthroughs will work and help you accomplish your
        tasks.
      </p>

      <p>
        Each Walkthrough has an embeddable version: a widget created from screenshots of the Walkthrough’s steps.
        <strong>Screenshots in the widget are automatically updated</strong>, so you don’t have to worry about
        previously embedded
        widgets when editing a Walkthrough: they will always contain the most up-to-date information. </p>
    </div>
