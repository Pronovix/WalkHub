      <h6>Reduce cognitive load</h6>

      <p><i>You will surely recognize this scenario: You are trying to accomplish a task on an online interface
          juggling your way through browser tabs, some written documentation or a video you constantly have to
          pause and rewind. </i></p>

      <p><strong>User guides and video tutorials</strong> are too complex and too sequential to offer users the
        help they need:
        They only <strong>increase users’ cognitive load</strong> because they have to switch back and forth
        between a window
        with the documentation (text or video) and the window in which they have the software open.
      </p>

      <p>
        We developed WalkHub to follow the way people actually do stuff online. Users get step by step
        instructions to accomplish their tasks without any distractions.
      </p>

      <p>
        We wanted to make our solution as intuitive as possible for Walkthrough creators as well. That is why,
        to create a Walkthrough, you record the process you want to share and document: simply click through a
        web site, fill in fields and submit forms. An initial description for steps is automatically generated -
        and if you have time, you can update them inline while playing the Walkthrough. </p>
