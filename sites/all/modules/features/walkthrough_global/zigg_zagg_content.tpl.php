      <h6>Reuse, share and collaborate</h6>

      <p><i>If you have ever had to rewrite a user guide and take new screenshots just because your software was used
          in a different environment, we guess you quickly ended up wishing for a reusable solution.</i></p>

      <p>Open source communities excel at sharing code, but are usually terrible at sharing documentation. We found
        that to get people to collaborate, a new tool had to be invented that makes it easy to <strong>create, reuse
          and
          share documentation between sites and projects.</strong> This tool is WalkHub. </p>

      <p>
        Walkthroughs are especially suited for making <strong>reusable chunks of content.</strong> They define a new
        format of
        tutorials that are strictly structured and that can easily be adapted to the visual context of a website. As
        a result it is much easier to <strong>reuse Walkthroughs between projects.</strong> WalkHub.net is also
        designed so that it
        could become the home of a range of community collections and their Walkthrough tutorials.

      </p>

